package main.classes;

public class SummaryThroughput {

    private int taktTime;
    private int workItems;
    private int projectLength;

    public int getTaktTime() {
        return taktTime;
    }

    public void setTaktTime(int taktTime) {
        this.taktTime = taktTime;
    }

    public int getWorkItems() {
        return workItems;
    }

    public void setWorkItems(int workItems) {
        this.workItems = workItems;
    }

    public int getProjectLength() {
        return projectLength;
    }

    public void setProjectLength(int projectLength) {
        this.projectLength = projectLength;
    }
}


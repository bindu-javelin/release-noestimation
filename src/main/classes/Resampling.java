package main.classes;

public class Resampling {

    private int taktTime;
    private int taktTimeCount;
    private int resampling;

    public int getTaktTimeCount() {
        return taktTimeCount;
    }

    public void setTaktTimeCount(int taktTimeCount) {
        this.taktTimeCount = taktTimeCount;
    }

    public int getTaktTime() {
        return taktTime;
    }

    public void setTaktTime(int taktTime) {
        this.taktTime = taktTime;
    }

    public int getResampling() {
        return resampling;
    }

    public void setResampling(int resampling) {
        this.resampling = resampling;
    }


}
